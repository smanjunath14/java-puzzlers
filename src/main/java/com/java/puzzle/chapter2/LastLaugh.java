package com.java.puzzle.chapter2;

public class LastLaugh {

    public static void lastLaugh(){
        System.out.println("H"+"a");
        System.out.println('H'+'a');
        System.out.println("2 + 2 = " + 2+2);
        System.out.printf("%c%c",'H','a');
    }

    public static void main(String[] args) {
        lastLaugh();
    }
}
